    public function create(Request $request): mixed
    {
        $draftUid    = (int) $request->query('draft_uid', 0);
        $draftFolder = (string) $request->query('draft_folder', '');

        // Pre-fill from an existing draft when ?draft_uid= is present.
        if ($draftUid > 0 && $draftFolder !== '') {
            $draft = $this->fetchOriginal($draftFolder, $draftUid);
            if ($draft !== null) {
                $to  = implode(', ', array_map(
                    fn($a) => $a['name'] ? "\"{$a['name']}\" <{$a['email']}>" : $a['email'],
                    $draft['to'],
                ));
                $cc  = implode(', ', array_map(
                    fn($a) => $a['name'] ? "\"{$a['name']}\" <{$a['email']}>" : $a['email'],
                    $draft['cc'],
                ));
                $body = $draft['body_html'] !== ''
                    ? $draft['body_html']
                    : $this->toHtml($draft['body_text']);
                return $this->showCompose([
                    'mode'         => 'compose',
                    'to'           => $to,
                    'cc'           => $cc,
                    'subject'      => $draft['subject'] !== '(no subject)' ? $draft['subject'] : '',
                    'body'         => $body,
                    'draftUid'     => $draftUid,
                    'draftFolder'  => $draftFolder,
                ]);
            }
        }

        return $this->showCompose([
            'mode'        => 'compose',
            'to'          => $request->query('to', ''),
            'subject'     => '',
            'body'        => '',
            'cc'          => '',
            'draftUid'    => 0,
            'draftFolder' => '',
        ]);
    }

    public function reply(Request $request, string $folder, string $uid): mixed
    {
        $original = $this->fetchOriginal($folder, (int) $uid);

        if ($original === null) {
            return redirect()->route('inbox');
        }

        $subject = $original['subject'];
        if (!preg_match('/^re:/i', $subject)) {
            $subject = 'Re: ' . $subject;
        }

        $from = $original['from'];
        $replyTo = $from['name']
            ? "\"{$from['name']}\" <{$from['email']}>"
            : $from['email'];

        return $this->showCompose([
            'mode'           => 'reply',
            'to'             => $replyTo,
            'subject'        => $subject,
            'body'           => $this->toHtml($this->quoteForReply($original)),
            'cc'             => '',
            'originalFolder' => $folder,
            'originalUid'    => (int) $uid,
            'draftUid'       => 0,
            'draftFolder'    => '',
        ]);
    }

    public function forward(Request $request, string $folder, string $uid): mixed
    {
        $original = $this->fetchOriginal($folder, (int) $uid);

        if ($original === null) {
            return redirect()->route('inbox');
        }

        $subject = $original['subject'];
        if (!preg_match('/^fwd:/i', $subject)) {
            $subject = 'Fwd: ' . $subject;
        }

        return $this->showCompose([
            'mode'           => 'forward',
            'to'             => '',
            'subject'        => $subject,
            'body'           => $this->toHtml($this->quoteForForward($original)),
            'cc'             => '',
            'originalFolder' => $folder,
            'originalUid'    => (int) $uid,
            'draftUid'       => 0,
            'draftFolder'    => '',
        ]);
    }

