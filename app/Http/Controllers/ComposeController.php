    /**
     * Check whether the authenticated user is allowed to send right now.
     *
     * Returns a human-readable error string when a limit is exceeded,
     * or null when sending is permitted.
     */
    private function checkSendLimits(string $email, int $recipientCount): ?string
    {
        $settings = userSettings();

        // Resolve limits: group overrides global defaults
        if ($settings->group_id) {
            $group    = MailGroup::find($settings->group_id);
            $hourly   = $group?->hourly_limit;
            $daily    = $group?->daily_limit;
            $weekly   = $group?->weekly_limit;
            $monthly  = $group?->monthly_limit;
            $maxRcpt  = $group?->max_recipients;
        } else {
            $hourly   = AdminSetting::get('default_hourly_limit');
            $daily    = AdminSetting::get('default_daily_limit');
            $weekly   = AdminSetting::get('default_weekly_limit');
            $monthly  = AdminSetting::get('default_monthly_limit');
            $maxRcpt  = AdminSetting::get('default_max_recipients');
        }

        // Per-message recipient cap
        if ($maxRcpt !== null && $recipientCount > (int) $maxRcpt) {
            return "You can address at most {$maxRcpt} recipients per message (you have {$recipientCount}).";
        }

        $now  = Carbon::now();
        $base = MailSendLog::where('email', $email)->where('status', 'sent');

        if ($hourly !== null) {
            $count = (clone $base)->where('created_at', '>=', $now->copy()->subHour())->count();
            if ($count >= (int) $hourly) {
                return "Hourly sending limit of {$hourly} reached. Please try again later.";
            }
        }

        if ($daily !== null) {
            $count = (clone $base)->where('created_at', '>=', $now->copy()->startOfDay())->count();
            if ($count >= (int) $daily) {
                return "Daily sending limit of {$daily} reached.";
            }
        }

        if ($weekly !== null) {
            $count = (clone $base)->where('created_at', '>=', $now->copy()->startOfWeek())->count();
            if ($count >= (int) $weekly) {
                return "Weekly sending limit of {$weekly} reached.";
            }
        }

        if ($monthly !== null) {
            $count = (clone $base)->where('created_at', '>=', $now->copy()->startOfMonth())->count();
            if ($count >= (int) $monthly) {
                return "Monthly sending limit of {$monthly} reached.";
            }
        }

        return null;
    }

    private function showCompose(array $compose): mixed
    {
        /** @var ImapGuard $guard */
        $guard    = auth('web');
        $settings = userSettings();
        $folders  = $this->fetchFolders($guard);

        // Inject signature — placed before any quoted body text
        $sig = trim($settings->signature ?? '');
        if ($sig !== '') {
            $sigBlock          = "\n\n-- \n{$sig}";
            $existing          = $compose['body'];
            $compose['body']   = $existing !== ''
                ? $sigBlock . "\n\n" . $existing
                : $sigBlock;
        }

        return view(mailView('compose.index'), array_merge($compose, [
            'folders'       => $folders,
            'currentFolder' => '',
            'fromEmail'     => $guard->user()->email,
            'fromName'      => $settings->display_name ?: $guard->user()->name,
            'draftUid'      => $compose['draftUid']    ?? 0,
            'draftFolder'   => $compose['draftFolder'] ?? '',
        ]));
    }

    /**
     * APPEND the just-sent message to the user's Sent folder. Creates the
     * folder if it doesn't exist. Silent on failure — delivery already
     * succeeded by the time this runs.
     */
    private function saveToSent(ImapGuard $guard, string $rawMessage): void
    {
        $imap = new ImapConnection();
        try {
            $imap->connect(
                host:         config('imap.host'),
                port:         config('imap.port'),
                encryption:   config('imap.encryption'),
                validateCert: config('imap.validate_cert', false),
                timeout:      config('imap.timeout', 10),
            );
            $imap->login($guard->getImapLogin(), $guard->getImapPassword());

            $sentFolder = $this->resolveSentFolder($imap);

            // APPEND with \Seen so it doesn't show as unread in Sent.
            if (!$imap->appendMessage($sentFolder, $rawMessage, ['\\Seen'])) {
                // First attempt failed — folder may not exist. Try to create it then retry once.
                if ($imap->createFolder($sentFolder)) {
                    $imap->appendMessage($sentFolder, $rawMessage, ['\\Seen']);
                }
            }

            $imap->logout();
        } catch (\Throwable) {
            $imap->close();
        }
    }

    /**
     * Delete a single draft message. Silent on failure.
     */
    private function deleteDraft(ImapGuard $guard, string $folder, int $uid): void
    {
        $imap = new ImapConnection();
        try {
            $imap->connect(
                host:         config('imap.host'),
                port:         config('imap.port'),
                encryption:   config('imap.encryption'),
                validateCert: config('imap.validate_cert', false),
                timeout:      config('imap.timeout', 10),
            );
            $imap->login($guard->getImapLogin(), $guard->getImapPassword());
            $imap->selectFolder($folder);
            $imap->deleteMessages((string) $uid);
            $imap->logout();
        } catch (\Throwable) {
            $imap->close();
        }
    }

    /**
     * Build a minimal RFC 5322 message suitable for saving as a draft.
     * Uses Symfony Mime so encoding and header folding are handled correctly.
     */
    private function buildDraftRaw(
        string $fromEmail,
        string $fromName,
        string $to,
        string $cc,
        string $bcc,
        string $subject,
        string $body,
    ): string {
        $htmlBody  = $body !== '' ? $body : '<p><br></p>';
        $plainBody = strip_tags(str_replace(['</p>', '<br>', '<br/>', '<br />'], "\n", $htmlBody));

        $email = (new Email())
            ->from(new Address($fromEmail, $fromName ?: $fromEmail))
            ->subject($subject ?: '(no subject)')
            ->html($htmlBody)
            ->text(trim($plainBody));

        foreach ($this->splitAddresses($to)  as $a) { $email->addTo($a); }
        foreach ($this->splitAddresses($cc)  as $a) { $email->addCc($a); }
        foreach ($this->splitAddresses($bcc) as $a) { $email->addBcc($a); }

        return $email->toString();
    }

    /**
     * Parse a comma/semicolon-separated address string into Symfony Address objects.
     *
     * @return Address[]
     */
    private function splitAddresses(string $raw): array
    {
        if (trim($raw) === '') {
            return [];
        }
        $addresses = [];
        foreach (preg_split('/[,;]/', $raw) ?: [] as $part) {
            $part = trim($part);
            if ($part === '') {
                continue;
            }
            try {
                if (preg_match('/^"?(.+?)"?\s*<([^>]+)>$/', $part, $m)) {
                    $addresses[] = new Address(trim($m[2]), trim($m[1], ' "'));
                } else {
                    $addresses[] = new Address($part);
                }
            } catch (\Throwable) {
                // Skip malformed addresses rather than aborting the draft save.
            }
        }
        return $addresses;
    }

    /**
     * Find the Drafts folder by SPECIAL-USE \Drafts attribute, falling back
     * to common naming conventions, then plain "Drafts".
     */
    private function resolveDraftsFolder(ImapConnection $imap): string
    {
        try {
            $folders = $imap->listFolders();
        } catch (\Throwable) {
            return 'Drafts';
        }

        foreach ($folders as $folder) {
            if (in_array('\\drafts', $folder['attributes'], true)) {
                return $folder['name'];
            }
        }

        $candidates = ['Drafts', 'INBOX.Drafts', 'Draft'];
        foreach ($candidates as $candidate) {
            foreach ($folders as $folder) {
                if (strcasecmp($folder['name'], $candidate) === 0) {
                    return $folder['name'];
                }
            }
        }

        return 'Drafts';
    }

    /**
     * Find the user's Sent folder by SPECIAL-USE \Sent attribute, falling
     * back to common naming conventions, then plain "Sent".
     */
    private function resolveSentFolder(ImapConnection $imap): string
    {
        try {
            $folders = $imap->listFolders();
        } catch (\Throwable) {
            return 'Sent';
        }

        // 1. Server-advertised \Sent SPECIAL-USE attribute.
        foreach ($folders as $folder) {
            if (in_array('\\sent', $folder['attributes'], true)) {
                return $folder['name'];
            }
        }

        // 2. Common case-insensitive names (covers Sent, Sent Items, INBOX.Sent).
        $candidates = ['Sent', 'INBOX.Sent', 'Sent Items', 'Sent Messages'];
        foreach ($candidates as $candidate) {
            foreach ($folders as $folder) {
                if (strcasecmp($folder['name'], $candidate) === 0) {
                    return $folder['name'];
                }
            }
        }

        return 'Sent';
    }

    private function fetchFolders(ImapGuard $guard): array
    {
        $imap = new ImapConnection();
        try {
            $imap->connect(
                host:         config('imap.host'),
                port:         config('imap.port'),
                encryption:   config('imap.encryption'),
                validateCert: config('imap.validate_cert', false),
                timeout:      config('imap.timeout', 10),
            );
            $imap->login($guard->getImapLogin(), $guard->getImapPassword());
            $folders = array_map(
                fn(array $f) => array_merge($f, $imap->getFolderStatus($f['name'])),
                $imap->listFolders()
            );
            $imap->logout();
            return $folders;
        } catch (\Throwable) {
            $imap->close();
            return [];
        }
    }

    private function fetchOriginal(string $folder, int $uid): ?array
    {
        /** @var ImapGuard $guard */
        $guard    = auth('web');
        $imap     = new ImapConnection();

        try {
            $imap->connect(
                host:         config('imap.host'),
                port:         config('imap.port'),
                encryption:   config('imap.encryption'),
                validateCert: config('imap.validate_cert', false),
                timeout:      config('imap.timeout', 10),
            );
            $imap->login($guard->getImapLogin(), $guard->getImapPassword());
            $imap->selectFolder($folder);
            $fetched = $imap->fetchMessageRaw($uid);
            $imap->logout();
        } catch (\Throwable) {
            $imap->close();
            return null;
        }

        if ($fetched['raw'] === '') {
            return null;
        }

        $parsed            = (new MimeParser())->parse($fetched['raw']);
        $parsed['uid']     = $uid;
        $parsed['folder']  = $folder;

        try {
            $dt = Carbon::parse($parsed['date_raw']);
            $parsed['date_formatted'] = $dt->format('l, F j, Y \a\t g:i A');
        } catch (\Throwable) {
            $parsed['date_formatted'] = $parsed['date_raw'];
        }

        return $parsed;
    }

    /**
     * Convert a plain-text body to simple HTML suitable for the rich-text editor.
     * Lines starting with "> " become <blockquote> content.
     * Already-HTML strings (start with a tag) are returned unchanged.
     */
    private function toHtml(string $text): string
    {
        if ($text === '') {
            return '';
        }
        if (preg_match('/^\s*</s', $text)) {
            return $text;
        }

        $lines     = explode("\n", $text);
        $out       = '';
        $quoteLines = [];

        foreach ($lines as $line) {
            if (str_starts_with($line, '> ') || $line === '>') {
                $quoteLines[] = htmlspecialchars(substr($line, 2), ENT_QUOTES | ENT_HTML5, 'UTF-8');
            } else {
                if ($quoteLines !== []) {
                    $out .= '<blockquote>' . implode('<br>', $quoteLines) . '</blockquote>';
                    $quoteLines = [];
                }
                $escaped = htmlspecialchars($line, ENT_QUOTES | ENT_HTML5, 'UTF-8');
                $out .= $escaped !== '' ? '<p>' . $escaped . '</p>' : '<p><br></p>';
            }
        }
        if ($quoteLines !== []) {
            $out .= '<blockquote>' . implode('<br>', $quoteLines) . '</blockquote>';
        }

        return $out;
    }

    private function quoteForReply(array $original): string
    {
        $date    = $original['date_formatted'] ?? $original['date_raw'];
        $from    = $original['from'];
        $sender  = $from['name'] ? "{$from['name']} <{$from['email']}>" : $from['email'];
        $body    = $this->plainBody($original);

        $quoted  = implode("\n", array_map(
            static fn(string $line): string => '> ' . $line,
            explode("\n", $body),
        ));

        return "\n\nOn {$date}, {$sender} wrote:\n{$quoted}";
    }

    private function quoteForForward(array $original): string
    {
        $date   = $original['date_formatted'] ?? $original['date_raw'];
        $from   = $original['from'];
        $sender = $from['name'] ? "{$from['name']} <{$from['email']}>" : $from['email'];
        $to     = implode(', ', array_map(
            static fn(array $a): string => $a['name'] ? "{$a['name']} <{$a['email']}>" : $a['email'],
            $original['to'],
        ));
        $body   = $this->plainBody($original);

        return "\n\n---------- Forwarded message ----------\n"
            . "From: {$sender}\n"
            . "Date: {$date}\n"
            . "Subject: {$original['subject']}\n"
            . "To: {$to}\n\n"
            . $body;
    }

    /** Get the best plain-text representation of a message body. */
    private function plainBody(array $message): string
    {
        if ($message['body_text'] !== '') {
            return rtrim($message['body_text']);
        }
        if ($message['body_html'] !== '') {
            return rtrim(wordwrap(strip_tags($message['body_html']), 80));
        }
        return '';
    }
