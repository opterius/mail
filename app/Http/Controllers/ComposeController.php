    public function send(Request $request): mixed
    {
        $maxMb = (int) config('smtp.max_attachment_mb', 25);

        $data = $request->validate([
            'to'            => ['required', 'string', 'max:2000'],
            'cc'            => ['nullable', 'string', 'max:2000'],
            'bcc'           => ['nullable', 'string', 'max:2000'],
            'subject'       => ['nullable', 'string', 'max:998'],
            'body'          => ['nullable', 'string'],
            'attachments'   => ['nullable', 'array'],
            'attachments.*' => ['file', 'max:' . ($maxMb * 1024)],
        ]);

        $attachments = $request->file('attachments', []);
        $totalBytes  = array_sum(array_map(fn($f) => $f->getSize(), $attachments));
        if ($totalBytes > $maxMb * 1024 * 1024) {
            return back()->withInput()->withErrors(['send' => "Total attachment size exceeds {$maxMb} MB."]);
        }

        /** @var ImapGuard $guard */
        $guard    = auth('web');
        $email    = $guard->user()->email;
        $name     = $guard->user()->name;
        $password = $guard->getImapPassword();

        $recipientCount = count(array_filter(
            array_merge(
                preg_split('/[,;]/', $data['to']) ?: [],
                preg_split('/[,;]/', $data['cc'] ?? '') ?: [],
                preg_split('/[,;]/', $data['bcc'] ?? '') ?: [],
            ),
            fn($a) => trim($a) !== '',
        ));

        $limitError = $this->checkSendLimits($email, $recipientCount);
        if ($limitError !== null) {
            return back()->withInput()->withErrors(['send' => $limitError]);
        }

        try {
            $rawMessage = (new SmtpSender())->send(
                fromEmail:    $email,
                fromName:     $name,
                password:     $password,
                to:           $data['to'],
                subject:      $data['subject'] ?? '',
                bodyHtml:     $data['body'] ?? '',
                cc:           $data['cc'] ?? '',
                bcc:          $data['bcc'] ?? '',
                authUsername: $guard->getImapLogin(),
                attachments:  $attachments,
            );

            MailSendLog::record(
                email:          $email,
                recipientCount: max(1, $recipientCount),
                subject:        $data['subject'] ?? '',
                ip:             $request->ip(),
                status:         'sent',
            );

            // Save a copy to the user's Sent folder. Failure here must not
            // surface to the user — the message was already delivered.
            $this->saveToSent($guard, $rawMessage);

            // Delete the draft that was being edited, if any.
            $draftUid    = (int) $request->input('draft_uid', 0);
            $draftFolder = (string) $request->input('draft_folder', '');
            if ($draftUid > 0 && $draftFolder !== '') {
                $this->deleteDraft($guard, $draftFolder, $draftUid);
            }
        } catch (\Throwable $e) {
            MailSendLog::record(
                email:          $email,
                recipientCount: max(1, $recipientCount),
                subject:        $data['subject'] ?? '',
                ip:             $request->ip(),
                status:         'failed',
            );

            return back()
                ->withInput()
                ->withErrors(['send' => 'Could not send message: ' . $e->getMessage()]);
        }

        return redirect()->route('inbox')->with('success', 'Message sent.');
    }

    public function saveDraft(Request $request): mixed
    {
        $data = $request->validate([
            'to'           => ['nullable', 'string', 'max:2000'],
            'cc'           => ['nullable', 'string', 'max:2000'],
            'bcc'          => ['nullable', 'string', 'max:2000'],
            'subject'      => ['nullable', 'string', 'max:998'],
            'body'         => ['nullable', 'string'],
            'draft_uid'    => ['nullable', 'integer', 'min:1'],
            'draft_folder' => ['nullable', 'string', 'max:500'],
        ]);

        /** @var ImapGuard $guard */
        $guard = auth('web');

        $raw = $this->buildDraftRaw(
            fromEmail: $guard->user()->email,
            fromName:  $guard->user()->name ?? '',
            to:        $data['to']      ?? '',
            cc:        $data['cc']      ?? '',
            bcc:       $data['bcc']     ?? '',
            subject:   $data['subject'] ?? '',
            body:      $data['body']    ?? '',
        );

        $imap = new ImapConnection();
        try {
            $imap->connect(
                host:         config('imap.host'),
                port:         config('imap.port'),
                encryption:   config('imap.encryption'),
                validateCert: config('imap.validate_cert', false),
                timeout:      config('imap.timeout', 10),
            );
            $imap->login($guard->getImapLogin(), $guard->getImapPassword());

            $draftsFolder = $this->resolveDraftsFolder($imap);

            // Delete the previous draft version before appending the new one.
            $oldUid = (int) ($data['draft_uid'] ?? 0);
            if ($oldUid > 0) {
                $prevFolder = $data['draft_folder'] ?? $draftsFolder;
                $imap->selectFolder($prevFolder);
                $imap->deleteMessages((string) $oldUid);
            }

            $newUid = $imap->appendGetUid($draftsFolder, $raw, ['\\Draft', '\\Seen']);

            $imap->logout();
        } catch (\Throwable) {
            $imap->close();
            return response()->json(['ok' => false], 500);
        }

        if ($newUid === -1) {
            return response()->json(['ok' => false], 500);
        }

        return response()->json([
            'ok'     => true,
            'uid'    => $newUid,   // 0 = server has no APPENDUID, >0 = trackable UID
            'folder' => $draftsFolder,
        ]);
    }

